public class BlankTextBean {

    /**
     * 文字
     */
    private String text;
    /**
     * 是否是填空
     */
    private boolean blank;

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public boolean isBlank() {
        return blank;
    }

    public void setBlank(boolean blank) {
        this.blank = blank;
    }
}
